    private void limparCampos() {
    txtProdutor.setText("");
    txtNomeEpisodio.setText("");
    txtNumeroEpisodio.setText("");
    txtDuracao.setText("");
    txtUrl.setText("");
}
    
    
    public boolean validarCampos() {
    // Validação para Produtor
    String regexProdutor = "^[a-zA-ZÀ-ÿ\\s]+$";
    if (!txtProdutor.getText().matches(regexProdutor)) {
        JOptionPane.showMessageDialog(this, "Nome do produtor inválido.");
        return false;
    }

    // Validação para Nome do Episódio
    String regexNomeEpisodio = "^[a-zA-Z0-9\\s\\-\\_]+$";
    if (!txtNomeEpisodio.getText().matches(regexNomeEpisodio)) {
        JOptionPane.showMessageDialog(this, "Nome do episódio inválido.");
        return false;
    }

    // Validação para Número do Episódio
    String regexNumeroEpisodio = "^\\d+$";
    if (!txtNumeroEpisodio.getText().matches(regexNumeroEpisodio)) {
        JOptionPane.showMessageDialog(this, "Número do episódio inválido.");
        return false;
    }

    // Validação para Duração
    String regexDuracao = "^([01]?\\d|2[0-3]):([0-5]?\\d)(:([0-5]?\\d))?$";
    if (!txtDuracao.getText().matches(regexDuracao)) {
        JOptionPane.showMessageDialog(this, "Duração' deve estar no formato HH:MM:SS.");
        return false;
    }

    // Validação para URL
    String regexUrl = "^(http://|https://)(www\\.)?([a-zA-Z0-9\\-]+)\\.([a-zA-Z]{2,})(/.*)?$";
    if (!txtUrl.getText().matches(regexUrl)) {
        JOptionPane.showMessageDialog(this, "URL inválida. digite conforme o exemplo: http://www.exemplo.com");
        return false;
    }

    return true;  // Se todos os campos estiverem válidos
}
    
    
