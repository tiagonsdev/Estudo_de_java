    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (validarCampos()) {
            // Se os campos forem válidos, cria o objeto Podcast e o salva
            Podcast podcast = new Podcast();
            podcast.setProdutor(txtProdutor.getText());
            podcast.setNomeEpisodio(txtNomeEpisodio.getText());
            podcast.setNumeroEpisodio(txtNumeroEpisodio.getText());
            podcast.setDuracao(txtDuracao.getText());
            podcast.setUrl(txtUrl.getText());

            // Chama o método de salvar no DAO
            PodcastDao dao = new PodcastDao();
            if (dao.conectar()) {
                int status = dao.salvar(podcast);
                if (status > 0) {
                    JOptionPane.showMessageDialog(null, "Podcast cadastrado com sucesso!");
                    // Limpar os campos após cadastro
                    limparCampos();
                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao cadastrar podcast.");
                }
                dao.desconectar();
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao conectar ao banco de dados.");
            }
        }
            
        
         
    }//GEN-LAST:event_jButton2ActionPerformed

